package coreJava;

public class ElseIf
{			
	public static void main (String args[])
	{
		int time = (16);
		
		if (time > 18)
		{
			System.out.println("good afternoon");
		}
		else if (time < 12)
		{
			System.out.println("good morning");
		}
		else
		{
			System.out.println("good night");
		}
		
	}	

}

